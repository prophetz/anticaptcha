<?php

namespace Prophetz\Anticaptcha\Client;


abstract class AbstractClient
{
    /**
     * @var string
     */
    private $domain;
    /**
     * @var string
     */
    private $key;

    /**
     * @return string
     */
    public function getDomain()
    {
        return $this->domain;
    }

    /**
     * @param string $domain
     */
    public function setDomain($domain)
    {
        $this->domain = $domain;
    }

    /**
     * @return string
     */
    public function getKey()
    {
        return $this->key;
    }

    /**
     * @param string $key
     */
    public function setKey($key)
    {
        $this->key = $key;
    }
}